        <h1 class="mb-4">Política de Privacidad</h1>
        <p><strong>Última actualización: 01/09/2024</strong></p>
        <p>En TechSolutions, respetamos su privacidad y estamos comprometidos a proteger la información personal que nos proporciona. Esta Política de Privacidad describe cómo recopilamos, usamos y compartimos su información personal.</p>

        <h2>1. Información que Recopilamos</h2>
        <p>Recopilamos información que usted nos proporciona directamente, como cuando se registra en nuestro sitio web, realiza una compra o se pone en contacto con nosotros. Esto puede incluir su nombre, dirección de correo electrónico, número de teléfono y otra información de contacto.</p>

        <h2>2. Uso de la Información</h2>
        <p>Utilizamos su información para proporcionar y mejorar nuestros servicios, procesar transacciones, y comunicarnos con usted sobre su cuenta y nuestras ofertas. También podemos usar su información para enviarle boletines informativos y promociones, si opta por recibirlos.</p>

        <h2>3. Compartición de Información</h2>
        <p>No vendemos ni alquilamos su información personal a terceros. Podemos compartir su información con proveedores de servicios que nos ayudan a operar nuestro negocio, pero solo en la medida necesaria para proporcionar esos servicios.</p>

        <h2>4. Seguridad</h2>
        <p>Implementamos medidas de seguridad para proteger su información personal contra el acceso no autorizado, la alteración, divulgación o destrucción. Sin embargo, ninguna medida de seguridad es completamente infalible, y no podemos garantizar la seguridad absoluta de su información.</p>

        <h2>5. Sus Derechos</h2>
        <p>Usted tiene derecho a acceder, corregir o eliminar su información personal. Para hacerlo, puede ponerse en contacto con nosotros utilizando los detalles que se indican a continuación.</p>

        <h2>6. Cambios en la Política</h2>
        <p>Podemos actualizar esta Política de Privacidad de vez en cuando. Le notificaremos sobre cualquier cambio publicando la nueva política en nuestro sitio web.</p>

